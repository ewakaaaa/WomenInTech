"""Evaluate the baseline appeal.

Reads the appeal generated by ``baseline/llm_call.py`` and runs the shared
evaluator from ``src/eval.py`` against ``data/eval.json``.

    uv run python -m baseline.eval
"""

from __future__ import annotations

from pathlib import Path

from baseline.main import OUTPUT_PATH
from src.eval import evaluate


def main() -> None:
    appeal_text = Path(OUTPUT_PATH).read_text(encoding="utf-8")
    result = evaluate(appeal_text)

    print(f"Wynik: {result.covered}/{result.total} = {result.score:.0%}\n")
    for r in result.results:
        mark = "✅" if r.covered else "❌"
        print(f"{mark} {r.issue[:80]}")
        print(f"    → {r.reasoning}\n")


if __name__ == "__main__":
    main()
